## Cadenas de caracteres

Cadena = '\'Hola mundo\''
print(Cadena)
Cadena = '\tHola mundo\t'
print(Cadena)
Cadena = '\nHola mundo\n'
print(Cadena)
Cadena = r'\nHola mundo\n'
print(Cadena)
Cadena = '''hola
que tal
Mi ombre es jose'''
print(Cadena)
