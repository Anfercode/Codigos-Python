## Funciones sin retorno de valor

def saludar():
	print('hola mundo')
	
def saludar2(Nombre):
	print(f'hola {Nombre}')	
	
saludar()
saludar()
saludar2('Hola')
saludar2('Jose')

def Tablas_Multiplicar(Numero):
	for i in range(1,11):
		print(f'{Numero} * {i} = {Numero * i}')
		
