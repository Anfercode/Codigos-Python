#Listas

Lista = ['Lunes','Martes','Miercoles','Jueves','Viernes',45,253,1563.3,[1,2,3,False, True]]

print(Lista) # Lista completa
print(Lista[0]) # Lista solo un valor
print(Lista[-1]) # Lista solo un valor
print(Lista[1:5]) # Lista Rango (Sublista 1)
print(Lista[:4]) # Lista Rango (Sublista 2)
print(Lista[2:]) # Lista Rango (Sublista 3)