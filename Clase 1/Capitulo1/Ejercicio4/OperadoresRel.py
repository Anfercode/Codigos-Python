'''
Operadores Relacionales
-- > : mayor que
-- < : menor que
-- >= : mayor  o igual que
-- <= : menor  o igual que
-- != : Diferente
-- == : Igual
'''

#Menor que

Resultado = 10 < 20
print(Resultado)

#Mayor que

Resultado = 10 > 20
print(Resultado)

#igual que

Resultado = 10 == 20
print(Resultado)

#diferente que

Resultado = 10 != 20
print(Resultado)

#menor o igual que

Resultado = 10 <= 20
print(Resultado)

#Mayor o igual que

Resultado = 10 >= 20
print(Resultado)

